import { Bar } from "react-chartjs-2";

const PredictionChart = ({ probabilities }) => {
  const data = {
    labels: [
      "No DR",
      "Mild DR",
      "Moderate DR",
      "Severe DR",
      "Proliferative DR",
    ],
    datasets: [
      {
        label: "Prediction Probabilities",
        data: probabilities,
        backgroundColor: [
          "rgba(75, 192, 192, 0.2)",
          "rgba(54, 162, 235, 0.2)",
          "rgba(255, 206, 86, 0.2)",
          "rgba(153, 102, 255, 0.2)",
          "rgba(255, 159, 64, 0.2)",
        ],
        borderColor: [
          "rgba(75, 192, 192, 1)",
          "rgba(54, 162, 235, 1)",
          "rgba(255, 206, 86, 1)",
          "rgba(153, 102, 255, 1)",
          "rgba(255, 159, 64, 1)",
        ],
        borderWidth: 1,
      },
    ],
  };

  const options = {
    scales: {
      y: {
        beginAtZero: true,
      },
    },
  };

  return <Bar data={data} options={options} />;
};

export default PredictionChart;
